// pin mappings for typewriter board

// some model specific things

#define RIBBON_BLK 0x00
#define RIBBON_WHT 0x01

// following constants define pins

//carriage
#define CARR_00 0x02
#define CARR_01 0x03
#define CARR_02 0x00
#define CARR_03 0x00

//daisy
#define DASY_00 0x04
#define DASY_01 0x05
#define DASY_02 0x00
#define DASY_03 0x00
#define DASY_EN 0x07

//linefeed
#define LNFD_00 0x03
#define LNFD_01 0x04
#define LNFD_02 0x00
#define LNFD_03 0x00
#define LNFD_EN 0x06

// cover open sensor
#define COVR_00 0x00

// keyboard matrix
#define KEYM_00 0x00
#define KEYM_01 0x00
#define KEYM_02 0x00
#define KEYM_03 0x00
#define KEYM_04 0x00
#define KEYM_05 0x00
#define KEYM_06 0x00
#define KEYM_07 0x00

// speaker
#define SPKR_00 0x00

// lights
#define LED_00 0x00

// hammer
#define HAMMER 0x08

// ribbon
#define RIBBON 0x09

// LEOM
#define LEOM 0x0C
